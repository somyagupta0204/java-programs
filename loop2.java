import java.util.*;
public class loop2 {
    public static void main(String args[]){
        Scanner input=new Scanner(System.in);
        System.out.println("enter the value of n");
        int n=input.nextInt();
        int i=n;
        while(i>=1){
            System.out.println(i);
            i--;
        }
    }
    
}
