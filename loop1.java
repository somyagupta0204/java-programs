import java.util.*;
public class loop1 {
    public static void main(String args[]){
        Scanner input=new Scanner (System.in);
        System.out.println("enter the value of n");
        int n=input.nextInt();
        int i=1;
        while(i<=n){
            System.out.println(i);
            i++;
        }
    }
    
}
